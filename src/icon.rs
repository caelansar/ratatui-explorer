//! File icon mappings for the file explorer.
//!
//! Icons are sourced from Nerd Fonts and require a Nerd Font-compatible terminal.
//! Icon data is based on [yazi's](https://github.com/sxyazi/yazi) icon theme.

use ratatui::style::Color;

use crate::File;

/// Controls how file icons are displayed.
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq, Hash)]
pub enum IconDisplay {
    /// No icons displayed (default).
    #[default]
    None,
    /// Icons displayed without per-icon colors (inherit row style).
    Plain,
    /// Icons with colors suited for dark terminal backgrounds.
    Dark,
    /// Icons with colors suited for light terminal backgrounds.
    Light,
}

impl IconDisplay {
    /// Returns `true` if icons should be rendered.
    pub fn is_enabled(self) -> bool {
        !matches!(self, Self::None)
    }
}

/// A file icon with optional foreground color.
#[derive(Debug, Clone, Copy)]
pub(crate) struct Icon {
    pub text: &'static str,
    pub color: Option<Color>,
}

/// An icon definition with both dark and light theme colors.
struct IconDef {
    text: &'static str,
    dark: Color,
    light: Color,
}

const fn icd(text: &'static str, dr: u8, dg: u8, db: u8, lr: u8, lg: u8, lb: u8) -> IconDef {
    IconDef {
        text,
        dark: Color::Rgb(dr, dg, db),
        light: Color::Rgb(lr, lg, lb),
    }
}

impl IconDef {
    const fn resolve(&self, display: IconDisplay) -> Icon {
        Icon {
            text: self.text,
            color: match display {
                IconDisplay::Dark => Some(self.dark),
                IconDisplay::Light => Some(self.light),
                IconDisplay::None | IconDisplay::Plain => None,
            },
        }
    }
}

